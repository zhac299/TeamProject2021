/**
 * Ingredient object with id, name, quantity and price per item
 */
export class Ingredient {

  /**
   * The id of the ingredient.
   */
  id: number;

  /**
   * The name of the ingredient.
   */
  name: string;

  /**
   * The quantity of the ingredient.
   */
  quantity: number;

  /**
   * The price per each item.
   */
  pricePerItem: number;
}
